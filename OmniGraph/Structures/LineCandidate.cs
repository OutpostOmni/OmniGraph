namespace OmniGraph.Structures {
    // Represents a possible line in a 2D grid.
    public class LineCandidate {
        public readonly Point Start;
        public readonly Point Step;
        public readonly Line Baseline;

        public LineCandidate(Point start, Point step) : this(start, step, null) {}

        public LineCandidate(Point start, Point step, Line baseline) {
            this.Start = start;
            this.Step = step;
            this.Baseline = baseline;
        }
    }
}
